public class robot
{
    public String head;
    public String leg;

    public void makeHead(String head)
    {
        this.head = head;
    }
    public String getHead()
    {
        return this.head;
    }

    public void makeLeg(String leg)
    {
        this.leg = leg;
    }
    public String getLeg()
    {
        return this.leg;
    }
}